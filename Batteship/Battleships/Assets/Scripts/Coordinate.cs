public class Coordinate {
    private int coordinateX;
    private int coordinateY;

    public int getCoordinateX()
    {
        return coordinateX;
    }

    public int getCoordinateY()
    {
        return coordinateY;
    }
	
    public void setCoordinateX(int coordX)
    {
        coordinateX = coordX;
    }

    public void setCoordinateY (int coordY)
    {
        coordinateY = coordY;
    }
}
